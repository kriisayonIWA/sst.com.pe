  function updateLanguage(element) {
    // Obtener el idioma del elemento clickeado o el idioma actual del navegador
    const lang = element ? element.getAttribute('data-lang') : window.navigator.language || 'es';
  
    // Cambiar el idioma actual de i18next
    i18next.changeLanguage(lang, function(err, t) {
      if (err) return console.error('Error al cambiar el idioma:', err);
      
      // Actualizar todos los elementos con la propiedad data-i18n
      document.querySelectorAll('[data-i18n]').forEach(function(element) {
        element.innerHTML = t(element.getAttribute('data-i18n'));
      });
  
      // Actualizar el idioma del texto "ES" o "EN" en la barra de idiomas
      document.querySelector('.idiom-change').innerHTML = lang.toUpperCase();
      
      console.log('Idioma actualizado a:', lang);
    });
  }
  
